def gettext(key: str, locale: str = "uz", **kwargs: Any) -> str:
    """
    Get a translated string for the given key and locale.

    Args:
        key: Translation key
        locale: Locale code (e.g., 'uz', 'ru')
        **kwargs: Format arguments for the translated string

    Returns:
        str: Translated string with placeholders replaced
    """
    translations = _get_translations(locale)
    text = translations.get(key, key)

    try:
        return text.format(**kwargs)
    except (KeyError, IndexError) as e:
        logger.warning("Translation formatting error for key '%s' in locale '%s': %s", key, locale, str(e))
        return text


def get_function(locale: str = "uz") -> Callable[..., str]:
    """
    Get a translation function for the specified locale.

    Args:
        locale: Locale code (e.g., 'uz', 'ru')

    Returns:
        callable: A function that takes a key and optional format arguments
    """

    def translate(key: str, **kwargs: Any) -> str:
        return gettext(key, locale, **kwargs)

    return translate


@lru_cache(maxsize=32)
def _get_translations(locale: str) -> dict[str, str]:
    """
    Get translations for the specified locale.

    Args:
        locale: Locale code (e.g., 'uz', 'ru')

    Returns:
        dict: Dictionary of translations for the locale
    """
    return DEFAULT_TRANSLATIONS.get(locale, DEFAULT_TRANSLATIONS["uz"])


def is_supported_language(locale: str) -> bool:
    """
    Check if a language is supported.

    Args:
        locale: Locale code to check

    Returns:
        bool: True if the language is supported, False otherwise
    """
    return locale in LANGUAGES


def get_language_info(locale: str) -> dict[str, str] | None:
    """
    Get information about a language.

    Args:
        locale: Locale code

    Returns:
        dict: Language information or None if not found
    """
    return LANGUAGES.get(locale)
